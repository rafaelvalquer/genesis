import fs from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import sharp from "sharp";

const ROOT = path.resolve(import.meta.dirname, "..");
const ENEMY_ROOT = path.join(ROOT, "src", "game", "assets", "enemy");
const OUTPUT = path.join(ROOT, "src", "game", "enemyAnchors.generated.js");
const AIRBORNE = new Set(["magoAbissal", "refrator"]);
const requested = new Set(process.argv.filter((argument) => argument.startsWith("--enemy=")).map((argument) => argument.slice(8)));

const frameNumber = (file) => Number(/(\d+)\.png$/i.exec(file)?.[1] || 0);
const quantile = (values, ratio) => {
  if (!values.length) return 0;
  values.sort((left, right) => left - right);
  return values[Math.min(values.length - 1, Math.floor((values.length - 1) * ratio))];
};
const rounded = (value) => Number(value.toFixed(4));

async function calculateAnchor(file, airborne) {
  const { data, info } = await sharp(file).ensureAlpha().raw().toBuffer({ resolveWithObject: true });
  const points = [];
  for (let y = 0; y < info.height; y += 1) {
    for (let x = 0; x < info.width; x += 1) {
      const offset = (y * info.width + x) * 4;
      const alpha = data[offset + 3];
      const luminance = data[offset] * 0.2126 + data[offset + 1] * 0.7152 + data[offset + 2] * 0.0722;
      if (alpha > 150 && luminance < 128 && x > info.width * 0.1 && x < info.width * 0.93) {
        points.push({ x, y, weight: Math.max(0.2, Math.min(1, (138 - luminance) / 80)) });
      }
    }
  }
  if (points.length < 80) {
    points.length = 0;
    for (let y = 0; y < info.height; y += 1) {
      for (let x = 0; x < info.width; x += 1) {
        const alpha = data[(y * info.width + x) * 4 + 3];
        if (alpha > 150) points.push({ x, y, weight: 1 });
      }
    }
  }
  const total = points.reduce((sum, point) => sum + point.weight, 0) || 1;
  const x = points.reduce((sum, point) => sum + point.x * point.weight, 0) / total;
  const y = airborne
    ? points.reduce((sum, point) => sum + point.y * point.weight, 0) / total
    : quantile(points.map((point) => point.y), 0.94);
  return { x: rounded(x / info.width), y: rounded(y / info.height) };
}

async function buildAnchors() {
  const entries = await fs.readdir(ENEMY_ROOT, { withFileTypes: true });
  const result = {};
  for (const enemy of entries) {
    if (!enemy.isDirectory() || enemy.name === "concepts" || (requested.size && !requested.has(enemy.name))) continue;
    const states = await fs.readdir(path.join(ENEMY_ROOT, enemy.name), { withFileTypes: true });
    const stateAnchors = {};
    for (const state of states) {
      if (!state.isDirectory()) continue;
      const directory = path.join(ENEMY_ROOT, enemy.name, state.name);
      const frames = (await fs.readdir(directory))
        .filter((file) => file.toLowerCase().endsWith(".png"))
        .sort((left, right) => frameNumber(left) - frameNumber(right));
      if (!frames.length) continue;
      stateAnchors[state.name] = [];
      for (const frame of frames) {
        stateAnchors[state.name].push(await calculateAnchor(path.join(directory, frame), AIRBORNE.has(enemy.name)));
      }
    }
    if (Object.keys(stateAnchors).length) result[enemy.name] = stateAnchors;
  }
  return result;
}

const anchors = await buildAnchors();
const lines = [
  "// Generated by scripts/generate-enemy-anchors.mjs. Do not edit by hand.",
  "const repeat = (anchor, count) => Array.from({ length: count }, () => anchor);",
  "export const ENEMY_FRAME_ANCHORS = {",
];
for (const [enemy, states] of Object.entries(anchors)) {
  lines.push(`  ${JSON.stringify(enemy)}: {`);
  for (const [state, frames] of Object.entries(states)) {
    const x = quantile(frames.map((anchor) => anchor.x), 0.5);
    const y = quantile(frames.map((anchor) => anchor.y), 0.5);
    lines.push(`    ${JSON.stringify(state)}: repeat({ x: ${x}, y: ${y} }, ${frames.length}),`);
  }
  lines.push("  },");
}
lines.push("};", "");
const source = lines.join("\n");
if (process.argv.includes("--write")) {
  await fs.writeFile(OUTPUT, source);
  console.log(`Wrote ${path.relative(ROOT, OUTPUT)}`);
} else {
  process.stdout.write(source);
}
